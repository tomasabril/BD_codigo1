    private void buscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscaActionPerformed
        // TODO add your handling code here:
        LivroTO tmp;
        tmp = livroDAO.find(textoBusca.getText());
        try {
            int tmpIndex = 0;
            for (LivroTO livro : listaLivro) {
                if (tmp.getLivro_id() == livro.getLivro_id()) {
                    indice = tmpIndex;
                    break;
                }
                tmpIndex++;
            }

            updateTable();

        } catch (Exception e1) {
            System.out.println("erro na busca: " + e1);
        }
    }//GEN-LAST:event_buscaActionPerformed

